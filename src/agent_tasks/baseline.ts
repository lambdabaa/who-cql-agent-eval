/**
 * Construct an AgentRunner from a colon-delimited id ("anthropic:claude-opus-4-7"
 * or "openai:gpt-5.5"). Throws if the provider isn't recognised or the
 * required API-key env var is missing.
 */
export function runnerFromId(spec: string): AgentRunner {
  const [provider, ...rest] = spec.split(':');
  const model = rest.join(':');
  if (!provider || !model) throw new Error(`agent spec must be "<provider>:<model>", got "${spec}"`);
  if (provider === 'anthropic') return anthropicRunner({ model });
  if (provider === 'openai') return openaiRunner({ model });
  throw new Error(`unknown provider "${provider}" (supported: anthropic, openai)`);
}

/**
 * Stage a canonical task into the agent's run dir (does not overwrite an
 * existing outputs/ unless `force` is true).
 */
function stageRun(taskDir: string, runDir: string, force: boolean): { staged: boolean; alreadyRun: boolean } {
  const outputs = join(runDir, 'outputs');
  if (existsSync(outputs) && !force) {
    const existing = readdirSync(outputs);
    if (existing.length > 0) return { staged: false, alreadyRun: true };
  }
  mkdirSync(runDir, { recursive: true });
  copyTree(taskDir, runDir, ['outputs']);
  mkdirSync(outputs, { recursive: true });
  return { staged: true, alreadyRun: false };
}

function copyTree(src: string, dst: string, skipTopLevelDirs: string[] = []) {
  for (const e of readdirSync(src)) {
    if (skipTopLevelDirs.includes(e)) continue;
    const sp = join(src, e);
    const dp = join(dst, e);
    const st = statSync(sp);
    if (st.isDirectory()) {
      mkdirSync(dp, { recursive: true });
      copyTree(sp, dp);
    } else {
      mkdirSync(join(dp, '..'), { recursive: true });
      copyFileSync(sp, dp);
    }
  }
}

/** Run one agent across all built tasks. */
export async function runAgentOnAllTasks(
  paths: BaselinePaths,
  runner: AgentRunner,
  opts: { force?: boolean } = {},
): Promise<AgentRunResult[]> {
  const results: AgentRunResult[] = [];
  for (const id of TASK_IDS) {
    const taskDir = join(paths.tasksRoot, id);
    if (!existsSync(taskDir)) throw new Error(`task fixture missing at ${taskDir} — run \`baseline build\` first`);
    const spec = readSpec(taskDir);
    const runDir = join(paths.runsRoot, sanitizeAgentId(runner.id), id);
    const { alreadyRun } = stageRun(taskDir, runDir, opts.force ?? false);
    if (alreadyRun) {
      console.log(`[${runner.id}/${id}] skip (already has outputs; pass --force to re-run)`);
      continue;
    }
    console.log(`[${runner.id}/${id}] calling agent...`);
    const r = await runner.run(runDir, spec);
    writeFileSync(join(runDir, 'run.json'), JSON.stringify(r, null, 2) + '\n');
    results.push(r);
    console.log(`[${runner.id}/${id}] done in ${r.durationMs}ms, wrote ${r.outputs.length} file(s)`);
    for (const w of r.warnings) console.warn(`  warn: ${w}`);
  }
  return results;
}

export interface GradedRun {
  agentId: string;
  taskId: string;
  spec: TaskSpec;
  result: GradeA1Result | GradeC2Result | GradeC4Result | GradeAuditResult | GradeCompositeC2Result;
}

/** Grade every agent run found under runs/. */
export async function gradeAllRuns(paths: BaselinePaths, opts: { jarPath?: string } = {}): Promise<GradedRun[]> {
  const out: GradedRun[] = [];
  if (!existsSync(paths.runsRoot)) return out;
  for (const agentDir of readdirSync(paths.runsRoot)) {
    const agentRoot = join(paths.runsRoot, agentDir);
    if (!statSync(agentRoot).isDirectory()) continue;
    for (const taskId of readdirSync(agentRoot)) {
      const runDir = join(agentRoot, taskId);
      if (!statSync(runDir).isDirectory()) continue;
      const spec = readSpec(runDir);
      if (spec.kind === 'authoring') {
        const submission = join(runDir, 'outputs', spec.outputFiles[0]!);
        const result = await gradeA1({
          spec,
          submissionPath: submission,
          dakRoot: paths.dakRoot,
          panelYaml: paths.panelYaml,
          workDir: join(runDir, '_grade'),
          ...(opts.jarPath ? { jarPath: opts.jarPath } : {}),
        });
        out.push({ agentId: agentDir, taskId, spec, result });
      } else if (spec.kind === 'prediction') {
        const result = gradeC4({ spec, taskDir: runDir, reportPath: join(runDir, 'grade.json') });
        out.push({ agentId: agentDir, taskId, spec, result });
      } else if (spec.kind === 'detection') {
        const result = gradeC2({ spec, taskDir: runDir, reportPath: join(runDir, 'grade.json') });
        out.push({ agentId: agentDir, taskId, spec, result });
      } else if (spec.kind === 'audit') {
        const result = gradeAudit({ spec, taskDir: runDir, reportPath: join(runDir, 'grade.json') });
        out.push({ agentId: agentDir, taskId, spec, result });
      } else if (spec.kind === 'composite_detection') {
        const result = gradeCompositeC2({ spec, taskDir: runDir, reportPath: join(runDir, 'grade.json') });
        out.push({ agentId: agentDir, taskId, spec, result });
      }
    }
  }
  return out;
}

export interface BaselineSummary {
  frozenAt: string;
  panelYaml: string;
  sourceRevision: string;
  agents: string[];
  tasks: TaskId[];
  rows: Array<{
    agentId: string;
    taskId: string;
    kind: TaskSpec['kind'];
    headline: string;
    detail: GradeA1Result | GradeC2Result | GradeC4Result | GradeAuditResult | GradeCompositeC2Result;
  }>;
}

/** Roll up grading results into one summary JSON written to baselines/<date>/. */
export function freezeBaseline(paths: BaselinePaths, graded: GradedRun[], dateIsoDay: string): string {
  const dir = join(paths.baselinesRoot, dateIsoDay);
  mkdirSync(dir, { recursive: true });
  const rows: BaselineSummary['rows'] = graded.map((g) => ({
    agentId: g.agentId,
    taskId: g.taskId as TaskId,
    kind: g.spec.kind,
    headline: headline(g.result),
    detail: g.result,
  }));
  // Source revision: read it off the augmented YAML's header comment if
  // present; fall back to "unknown".
  let sourceRevision = 'unknown';
  try {
    const head = readFileSync(paths.panelYaml, 'utf8').slice(0, 1024);
    const m = head.match(/smart-immunizations \(([a-f0-9]+)\)/);
    if (m) sourceRevision = `smart-immunizations@${m[1]}`;
  } catch {
    // leave as unknown
  }
  const summary: BaselineSummary = {
    frozenAt: new Date().toISOString(),
    panelYaml: relative(process.cwd(), paths.panelYaml),
    sourceRevision,
    agents: [...new Set(graded.map((g) => g.agentId))].sort(),
    tasks: [...new Set(graded.map((g) => g.taskId as TaskId))].sort() as TaskId[],
    rows,
  };
  const path = join(dir, 'summary.json');
  writeFileSync(path, JSON.stringify(summary, null, 2) + '\n');
  return path;
}

function headline(r: GradeA1Result | GradeC2Result | GradeC4Result | GradeAuditResult | GradeCompositeC2Result): string {
  if ('t1' in r) {
    if (!r.agentSubmitted) return 'no submission';
    if (r.t1 !== 'pass') return `T1: ${r.t1}`;
    const t3 = r.t3!;
    return `T1: pass · T3: ${t3.casesPassed}/${t3.casesTotal}`;
  }
  if ('detection' in r) {
    if (r.parseError && !r.agentSubmitted) return 'no submission';
    if (r.parseError) return `parse error: ${r.parseError}`;
    const d = r.detection;
    return `F1: ${d.f1.toFixed(2)} (P=${d.precision.toFixed(2)} R=${d.recall.toFixed(2)}) · loc: ${r.localization.defineCorrect}/${r.localization.flagged}`;
  }
  if ('global' in r) {
    if (r.parseError && !r.agentSubmitted) return 'no submission';
    if (r.parseError) return `parse error: ${r.parseError}`;
    const g = r.global;
    return `F1: ${g.f1.toFixed(2)} (P=${g.precision.toFixed(2)} R=${g.recall.toFixed(2)}) · TP=${g.truePositive}/FP=${g.falsePositive}/FN=${g.falseNegative}`;
  }
  if ('findingsCount' in r) {
    if (r.parseError && !r.agentSubmitted) return 'no submission';
    if (r.parseError) return `parse error: ${r.parseError}`;
    return r.findingsCount === 0 ? 'clean (no findings)' : `${r.findingsCount} finding${r.findingsCount === 1 ? '' : 's'}`;
  }
  if (r.parseError) return `parse error: ${r.parseError}`;
  if (!r.agentSubmitted) return 'no submission';
  return `${r.correctCells}/${r.totalCells} predictions correct`;
}

function readSpec(taskDir: string): TaskSpec {
  const raw = JSON.parse(readFileSync(join(taskDir, 'task.json'), 'utf8'));
  return TaskSpecSchema.parse(raw);
}

function sanitizeAgentId(id: string): string {
  return id.replace(/[^a-zA-Z0-9_.-]/g, '_');
}
