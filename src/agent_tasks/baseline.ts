  buildAuditMeaslesLowTxFixture({ dakRoot: paths.dakRoot, taskDir: join(paths.tasksRoot, 'audit_measles_low_tx') });
  buildAuditMeaslesMcv0Fixture({ dakRoot: paths.dakRoot, taskDir: join(paths.tasksRoot, 'audit_measles_mcv0') });
