/**
 * Top-level baseline orchestration.
 *
 * The canonical task fixtures live under `tasks/`. Each agent run gets its
 * own copy of the fixture under `runs/<agent>/<task>/` so multiple agents
 * don't trample each other's outputs and so the call is fully reproducible
 * from the on-disk fixture.
 *
 * Layout:
 *
 *   tasks/<task-id>/         canonical, regenerated by `baseline build`
 *   runs/<agent>/<task-id>/  agent-specific copy + outputs + grade.json
 *   baselines/<date>/        frozen baseline JSON + summary
 */

export const TASK_IDS = [
  'A1_measles_low_tx',
  'C2_measles_low_tx',
  'C2_measles_mcv0',
  'C2_measles_ongoing_tx',
  'C2_measles_supplementary',
  'C2_anc_dt08',
  'composite_c2_measles_low_tx',
  'composite_c2_measles_mcv0',
  'composite_c2_measles_ongoing_tx',
  'C4_measles_low_tx',
  'audit_measles_low_tx',
  'audit_measles_mcv0',
  'audit_measles_ongoing_tx',
  'audit_measles_supplementary',
  'audit_anc_dt08',
] as const;
export type TaskId = (typeof TASK_IDS)[number];

export interface BaselinePaths {
  /** Path to vendored DAK. */
  dakRoot: string;
  /** Path to canonical task dirs. */
  tasksRoot: string;
  /** Path to per-agent run dirs. */
  runsRoot: string;
  /** Path to frozen baseline outputs. */
  baselinesRoot: string;
  /** Augmented YAML overlay. */
  panelYaml: string;
}

export function defaultBaselinePaths(repoRoot: string = process.cwd()): BaselinePaths {
  return {
    dakRoot: resolve(repoRoot, 'vendor', 'smart-immunizations'),
    tasksRoot: resolve(repoRoot, 'tasks'),
    runsRoot: resolve(repoRoot, 'runs'),
    baselinesRoot: resolve(repoRoot, 'baselines'),
    panelYaml: resolve(repoRoot, 'tests', 'dak', 'IMMZD2DTMeaslesLowTransmissionLogic.yaml'),
  };
}

/** Build canonical task fixtures under `tasks/`. */
